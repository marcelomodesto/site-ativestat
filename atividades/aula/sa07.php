						<p style="text-align: justify;">Nesta atividade, os estudantes simulam um
							procedimento para avaliar o número de peixes em um lago. Uma sacola simboliza o
							lago e as fichas os peixes. O número de fichas deve ser significantemente maior
							que o número de estudantes na sala. Discussões devem ser feitas sobre possíveis
							maneiras de estimar o número total de peixes. Uma delas será aplicada na
							atividade e é conhecida como o processo de “Captura e Recaptura”, que será
							descrito a seguir. Um determinado número de peixes (fichas) é capturado, marcado
							e devolvido ao lago (sacola). Após os peixes (fichas) se misturarem bem, uma
							recaptura é feita. Assim, é possível obter uma estimativa para o número de
							peixes no lago comparando duas proporções: a de peixes marcados em relação à
							população e a de peixes marcados escolhidos na recaptura. Após a estimação
							feita, os estudantes poderão, de fato, contar o número de fichas na sacola
							(peixes no lago). Atividade da oficina “Estatística para todos” desenvolvida
							pela professora Lisbeth K. Cordani.</p>
